"""type function in python is used to return the data-type of data that is stored in the
variable. We can use the type() function inside a print statement inside a paranthesis ]
followed by the variable name"""

#Example of type() function

a = 5 # a contain integer data-type
b = 5.0 # b contain float data-type
c = "this is the latest version of python" # c contains string data-type

print(a)
print(b)
print(c)

print(type(a))
print(type(b))
print(type(c))
